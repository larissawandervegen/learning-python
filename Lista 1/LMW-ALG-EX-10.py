import math
a = int(input("Qual é o primeiro número inteiro? "))
b = int(input("Qual é o segundo número inteiro? "))

print("A soma é igual a", a + b)
print("A diferença é igual a", b - a)
print("O quociente é igual a", a / b)
print("O resto é igual a", a % b)
print("O log é igual a", math.log(a, 10))
print("A potenciação é igual a", a ** b)